import fs from 'fs';
import path from 'path';

const ROOT = process.cwd();
const VERSION = 'V95_SAFE_CLEANUP_ACTIVE';
const REPORT = path.join(ROOT, 'assets', 'data', 'v95-safe-cleanup-report.json');

function walk(dir, predicate = () => true) {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === 'node_modules' || entry.name === '.git') continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full, predicate));
    else if (entry.isFile() && predicate(full)) out.push(full);
  }
  return out;
}
function rel(file) { return path.relative(ROOT, file).replace(/\\/g, '/'); }
function read(file) { return fs.readFileSync(file, 'utf8'); }
function write(file, text) { fs.mkdirSync(path.dirname(file), { recursive: true }); fs.writeFileSync(file, text); }
function exists(relPath) { return fs.existsSync(path.join(ROOT, relPath)); }
function size(relPath) { try { return fs.statSync(path.join(ROOT, relPath)).size; } catch { return 0; } }
function removeIfExists(relPath) {
  const abs = path.join(ROOT, relPath);
  if (!fs.existsSync(abs)) return false;
  fs.rmSync(abs, { force: true });
  return true;
}

function collectTextCorpus() {
  const files = walk(ROOT, file => /\.(html|css|js|mjs|json|xml|txt|toml)$/i.test(file));
  return files.map(file => {
    try { return { file: rel(file), text: read(file) }; }
    catch { return { file: rel(file), text: '' }; }
  });
}

function hasReference(corpus, relPath) {
  const variants = new Set([
    relPath,
    '/' + relPath,
    relPath.replace(/^assets\//, '/assets/'),
    relPath.split('/').pop()
  ]);
  const refs = [];
  for (const item of corpus) {
    if (item.file === relPath) continue;
    let hit = false;
    for (const v of variants) {
      if (v && item.text.includes(v)) { hit = true; break; }
    }
    if (hit) refs.push(item.file);
  }
  return refs;
}

function ensurePackageScripts() {
  const pkgPath = path.join(ROOT, 'package.json');
  const pkg = JSON.parse(read(pkgPath));
  pkg.scripts = pkg.scripts || {};
  const gen = 'node scripts/generate-v95-safe-cleanup.mjs';
  const ver = 'node scripts/verify-v95-safe-cleanup.mjs';
  const current = String(pkg.scripts.build || '').trim();
  if (!current.includes(gen)) pkg.scripts.build = current ? `${current} && ${gen}` : gen;
  pkg.scripts.verify = ver;
  pkg.scripts['quality:v95'] = gen;
  pkg.scripts['verify:v95'] = ver;
  write(pkgPath, JSON.stringify(pkg, null, 2));
}

function markHtml() {
  let changed = 0;
  for (const file of walk(ROOT, f => f.endsWith('.html'))) {
    let html = read(file);
    if (!/<html\b/i.test(html)) continue;
    if (html.includes('data-v95-safe-cleanup="active"')) continue;
    html = html.replace(/<html\b([^>]*)>/i, (m, attrs) => {
      if (/data-v95-safe-cleanup=/i.test(attrs)) return m;
      return `<html${attrs} data-v95-safe-cleanup="active">`;
    });
    write(file, html);
    changed += 1;
  }
  return changed;
}

function removeDuplicateLegacyReportData(corpus) {
  // Only remove deployment-output audit/report JSON files that are regenerated by older generators
  // and are not required by public pages, CSS, JS, sitemap, robots, or current V95 verification.
  const allowlist = [
    'assets/data/v84-performance-audit.json',
    'assets/data/v94-performance-pass-2.json',
    'assets/data/v93-tools-usability-pass.json'
  ];
  const deleted = [];
  const retained = [];
  for (const relPath of allowlist) {
    const refs = hasReference(corpus, relPath).filter(f => !f.startsWith('scripts/verify-v84') && !f.startsWith('scripts/verify-v94'));
    if (refs.length === 0 && exists(relPath)) {
      const before = size(relPath);
      removeIfExists(relPath);
      deleted.push({ path: relPath, bytes: before, reason: 'unreferenced generated audit/report JSON removed from deploy output' });
    } else {
      retained.push({ path: relPath, refs, reason: refs.length ? 'reference detected' : 'file absent' });
    }
  }
  return { deleted, retained };
}

function scanAssets(corpus) {
  const assetFiles = walk(path.join(ROOT, 'assets'), f => /\.(png|jpg|jpeg|webp|svg|gif|css|js|json)$/i.test(f));
  const unused = [];
  const referenced = [];
  for (const file of assetFiles) {
    const relPath = rel(file);
    if (relPath === 'assets/data/v95-safe-cleanup-report.json') continue;
    const refs = hasReference(corpus, relPath);
    const row = { path: relPath, bytes: fs.statSync(file).size, refs: refs.slice(0, 8), refCount: refs.length };
    if (refs.length === 0) unused.push(row);
    else referenced.push(row);
  }
  unused.sort((a, b) => b.bytes - a.bytes);
  referenced.sort((a, b) => b.bytes - a.bytes);
  return { unusedCandidates: unused.slice(0, 100), referencedLargeAssets: referenced.filter(x => x.bytes > 300 * 1024).slice(0, 50) };
}

function scanRootLegacy() {
  const rootFiles = walk(ROOT, f => path.dirname(f) === ROOT && /\.(png|ico|jpg|jpeg|webp|svg)$/i.test(f));
  return rootFiles.map(f => ({ path: rel(f), bytes: fs.statSync(f).size }));
}

ensurePackageScripts();
const changedHtml = markHtml();
const corpusBefore = collectTextCorpus();
const cleanup = removeDuplicateLegacyReportData(corpusBefore);
const corpusAfter = collectTextCorpus();
const audit = scanAssets(corpusAfter);
const rootLegacyFiles = scanRootLegacy();

const savedBytes = cleanup.deleted.reduce((sum, item) => sum + item.bytes, 0);
const report = {
  version: VERSION,
  generatedAt: new Date().toISOString(),
  policy: 'safe cleanup only: delete allowlisted generated reports with zero runtime references; keep all CSS/JS/image legacy candidates unless proven safe',
  summary: {
    changedHtml,
    deletedFiles: cleanup.deleted.length,
    savedBytes,
    unusedCandidates: audit.unusedCandidates.length,
    referencedLargeAssets: audit.referencedLargeAssets.length,
    rootLegacyFiles: rootLegacyFiles.length
  },
  deleted: cleanup.deleted,
  retainedAllowlist: cleanup.retained,
  unusedCandidates: audit.unusedCandidates,
  referencedLargeAssets: audit.referencedLargeAssets,
  rootLegacyFiles,
  deleteRules: [
    'Do not delete CSS/JS generated by previous versions while build chain still runs them.',
    'Do not delete vendor/blog/search/sports images unless every HTML/CSS/JS and generator reference is zero.',
    'PATCH uploads cannot physically delete repository files; V95 generator removes safe files from deploy output during build.',
    'Full cleanup should use FULL ZIP only after live deploy check passes.'
  ]
};
write(REPORT, JSON.stringify(report, null, 2));
console.log(`[V95] safe cleanup complete. html=${changedHtml} deleted=${cleanup.deleted.length} savedBytes=${savedBytes}`);
