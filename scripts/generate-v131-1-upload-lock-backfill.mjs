function abs(rel){ return path.join(ROOT, rel); }
function ensureDir(rel){ fs.mkdirSync(abs(rel), { recursive:true }); }
function exists(rel){ return fs.existsSync(abs(rel)); }
function read(rel){ return fs.readFileSync(abs(rel), 'utf8'); }
function write(rel, body){ ensureDir(path.dirname(rel)); fs.writeFileSync(abs(rel), body); }
function walk(dir=ROOT,out=[]){
  if(!fs.existsSync(dir)) return out;
  for(const name of fs.readdirSync(dir)){
    if(['node_modules','.git','.wrangler','.cache'].includes(name)) continue;
    const full = path.join(dir,name);
    const rel = path.relative(ROOT, full).replace(/\\/g,'/');
    const st = fs.statSync(full);
    if(st.isDirectory()) walk(full,out); else out.push(rel);
  }
  return out;
}
function ensureHead(html){
  if(!/<html\b/i.test(html)) html = '<!doctype html>\n<html lang="ko">\n' + html + '\n</html>';
  if(!/<head\b/i.test(html)) html = html.replace(/<html\b[^>]*>/i, (m)=>`${m}\n<head></head>`);
  return html;
}
function addHead(html, snippet){
  html = ensureHead(html);
  return html.replace(/<\/head>/i, `${snippet}\n</head>`);
}
function addAttrToTag(html, tag, attr, value){
  const re = new RegExp(`<${tag}\\b([^>]*)>`, 'i');
  if(!re.test(html)){
    if(tag === 'body') html = html.replace(/<\/head>/i, `</head>\n<body ${attr}="${value}"></body>`);
    return html;
  }
  return html.replace(re, (m, attrs='') => {
    if(new RegExp(`${attr}=`, 'i').test(attrs)) return m;
    return `<${tag}${attrs} ${attr}="${value}">`;
  });
}
function ensureLock(html, lock){
  html = ensureHead(html);
  html = addAttrToTag(html, 'html', lock.attr, 'active');
  html = addAttrToTag(html, 'body', lock.attr, 'true');
  if(!html.includes(path.basename(lock.file))){
    html = addHead(html, `  <link rel="stylesheet" href="${lock.css}" ${lock.attr}="true">`);
  }
  return html;
}
function removeForbiddenTokens(html){
  let out = html;
  for(const token of forbiddenMarkers){
    const escaped = token.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const block = new RegExp(`\\n?\\s*<(?:section|div|aside|nav)\\b[^>]*(?:class|id|data-[^=]+)=["'][^"']*${escaped}[^"']*["'][^>]*>[\\s\\S]*?<\\/(?:section|div|aside|nav)>\\s*`, 'gi');
    out = out.replace(block, '');
    out = out.split(token).join('');
  }
  return out;
}

